package tutoring01.prac;

public class Variable {
    public static void main(String[] args) {
        int a = 7;
        int b = 3;
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a % b;

        System.out.println("c : " + c);
        System.out.println("d : " + d);
        System.out.println("e : " + e);
        System.out.println("f : " + f);
    }


}
